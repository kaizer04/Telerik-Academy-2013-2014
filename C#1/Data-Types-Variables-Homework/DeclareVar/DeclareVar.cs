using System;

class DeclareVar
{
    static void Main()
    {
        ushort us = 52130;
        Console.WriteLine("ushort = " + us);
        sbyte sb = -115;
        Console.WriteLine("sbyte = " + sb);
        uint ui = 4825932;
        Console.WriteLine("uint = " + ui);
        byte b = 97;
        Console.WriteLine("byte = " + b);
        short s = -10000;
        Console.WriteLine("short = " + s);
    }
}

